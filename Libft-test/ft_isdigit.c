int ft_isdigit(int c)
{
    if (c <= '9' && c >= '0')
        return (1);
    else
        return (0);
}

#include<stdio.h>
int main()
{
    int c = '2';
    printf("%d\n", ft_isdigit(c));
    return(0);
}
